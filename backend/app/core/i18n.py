def normalize_lang(raw: str | None) -> str:
    lang = (raw or '').strip().lower()
    if lang in SUPPORTED_LANGS:
        return lang
    return DEFAULT_LANG


def get_request_lang(request: Request | None) -> str:
    if not request:
        return DEFAULT_LANG
    cookie_lang = normalize_lang(request.cookies.get('lang'))
    if cookie_lang in SUPPORTED_LANGS:
        return cookie_lang
    accept = (request.headers.get('accept-language') or '').lower()
    if accept.startswith('en'):
        return 'en'
    return DEFAULT_LANG


def t(key: str, lang: str | None = None) -> str:
    use_lang = normalize_lang(lang)
    item = TRANSLATIONS.get(key)
    if not item:
        return key
    return item.get(use_lang) or item.get(DEFAULT_LANG) or key


def tt(request: Request, key: str) -> str:
    return t(key, get_request_lang(request))
